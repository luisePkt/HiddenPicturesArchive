// Upload an image
const uploadToCloudinary = async (path, folder = "profilePics") => {
  try {
    const data = await cloudinary.uploader.upload(path, { folder: folder });
    return { url: data.secure_url, publicId: data.public_id };
  } catch (error) {
    console.log(error);
  }
};

